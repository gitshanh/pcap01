uint8_t pcap01_cmd8(pcap01_handle_t *pcap, uint8_t cmd)
{
    uint8_t ret;

    pcap->spi_cs_enable();
    ret = pcap->spi_write(&cmd, 1);
    pcap->spi_cs_disable();

    return ret;
}

uint8_t pcap01_cmd24(pcap01_handle_t *pcap, uint8_t cmd, uint16_t addr,
                           uint8_t tx_data, uint8_t *rx_data)
{
    uint8_t tx_buff[3] = {0};
    uint8_t rx_buff[3] = {0};
    uint8_t ret;

    tx_buff[0] = addr >> 8 | cmd;
    tx_buff[1] = addr;
    tx_buff[2] = tx_data;

    pcap->spi_cs_enable();

    if (cmd == PCAP01_CMD24_WRITE_SRAM || cmd == PCAP01_CMD24_WRITE_OTP)
    {
        ret = pcap->spi_write(tx_buff, 3);
    }
    else
    {
        ret = pcap->spi_write_read(tx_buff, rx_buff, 3);
        if (ret == 0)
        {
            *rx_data = rx_buff[2];
        }
    }

    pcap->spi_cs_disable();

    return ret;
}

uint8_t pcap01_cmd32(pcap01_handle_t *pcap, uint8_t cmd, uint16_t addr,
                     uint32_t tx_data, uint32_t *rx_data)
{
    uint8_t tx_buff[4] = {0};
    uint8_t rx_buff[4] = {0};
    uint8_t ret;

    tx_buff[0] = cmd | addr;
    tx_buff[1] = tx_data >> 16;
    tx_buff[2] = tx_data >> 8;
    tx_buff[3] = tx_data;

    pcap->spi_cs_enable();

    if (cmd == PCAP01_CMD32_WRITE_CONFIG)
    {
        ret = pcap->spi_write(tx_buff, 4);
    }
    else
    {
        ret = pcap->spi_write_read(tx_buff, rx_buff, 4);
        if (ret == 0)
        {
            *rx_data = (rx_buff[1] << 16) | (rx_buff[2] << 8) | rx_buff[3];
        }
    }

    pcap->spi_cs_disable();

    return ret;
}

//(Register 0..10)
uint8_t pcap01_write_config(pcap01_handle_t *pcap, uint32_t config[11])
{
    uint8_t ret = 0xff;
    uint32_t rx_data;
    for (int i = 0; i < 11; i++)
    {
        ret = pcap01_cmd32(pcap, PCAP01_CMD32_WRITE_CONFIG,
                           (uint16_t)i, config[i], &rx_data);
        if (ret != 0) break;
    }
    return ret;
}

//配置寄存器最后写入的参数，保证配置中dsp静止,配置完成后打开，进入运行模式
uint8_t pcap01_set_runbit(pcap01_handle_t *pcap, uint32_t run)
{
    uint32_t rx_data;
    return pcap01_cmd32(pcap, PCAP01_CMD32_WRITE_CONFIG,
                        0x14, run, &rx_data);
}

//单端或差分传感器
uint8_t pcap01_set_differential(pcap01_handle_t *pcap, uint32_t diff)
{
    uint32_t rx_data;
    return pcap01_cmd32(pcap, PCAP01_CMD32_WRITE_CONFIG,
                        0x12, diff, &rx_data);
}

uint8_t pcap01_write_sram(pcap01_handle_t *pcap, uint8_t firmware[4096])
{
    uint8_t rx_data;
    uint8_t ret;

    ret = pcap01_cmd24(pcap, PCAP01_CMD24_WRITE_SRAM, 0x1, 0x55, &rx_data);
    if (ret != 0) return ret;

    ret = pcap01_cmd24(pcap, PCAP01_CMD24_READ_SRAM, 0x1, DUMMY_WRITE, &rx_data);
    if (ret != 0) return ret;

    for (int i = 0; i < 4096; i++)
    {
        ret = pcap01_cmd24(pcap, PCAP01_CMD24_WRITE_SRAM, i, 0x0, &rx_data);
        if (ret != 0) break;
    }

    for (int i = 0; i < 16; i++)
    {
        ret = pcap01_cmd24(pcap, PCAP01_CMD24_READ_SRAM, i, DUMMY_WRITE, &rx_data);
        if (ret != 0) break;
    }

    for (int i = 0; i < 4096; i++)
    {
        ret = pcap01_cmd24(pcap, PCAP01_CMD24_WRITE_SRAM, i, firmware[i], &rx_data);
        if (ret != 0) break;
    }

    for (int i = 0; i < 16; i++)
    {
        ret = pcap01_cmd24(pcap, PCAP01_CMD24_READ_SRAM, i, DUMMY_WRITE, &rx_data);
        if (ret != 0) break;
    }

    return ret;
}
